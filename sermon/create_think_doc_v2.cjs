// ── Helper: body paragraph ─────────────────────────────────────────
function body(text, opts = {}) {
  return new Paragraph({
    spacing: { after: 150, line: 400 },
    ...opts.pOpts,
    children: [new TextRun({
      font: FONT, size: 21, color: C.body,
      ...opts.rOpts,
      text,
    })],
  });
}

// ── Helper: quote callout box (left gold border, cream bg) ─────────
function quoteBox(quoteText, source) {
  const noBorder = { style: BorderStyle.NONE, color: C.white, size: 0 };
  return new Table({
    width: { size: CONTENT_W, type: WidthType.DXA },
    columnWidths: [CONTENT_W],
    rows: [new TableRow({
      children: [new TableCell({
        width: { size: CONTENT_W, type: WidthType.DXA },
        borders: {
          top: noBorder, bottom: noBorder, right: noBorder,
          left: { style: BorderStyle.THICK, color: C.goldDk, size: 20 },
        },
        shading: { fill: C.quoteBg, type: ShadingType.CLEAR },
        margins: { top: 120, left: 280, bottom: 120, right: 200 },
        children: [
          new Paragraph({
            spacing: { after: 80 },
            children: [new TextRun({
              font: FONT, size: 20, color: C.quote, italics: true, text: quoteText,
            })],
          }),
          new Paragraph({
            spacing: { after: 0 },
            alignment: AlignmentType.RIGHT,
            children: [new TextRun({
              font: FONT, size: 18, color: C.goldDk, bold: true,
              text: `\u2014 ${source}`,
            })],
          }),
        ],
      })],
    })],
  });
}

// ── Helper: spacer ─────────────────────────────────────────────────
function spacer(after = 120) {
  return new Paragraph({ spacing: { after }, children: [new TextRun("")] });
}

// ── Helper: section header (gold label + navy title + bottom line) ─
function sectionHeader(label, title) {
  return [
    spacer(320),
    new Paragraph({
      spacing: { after: 30 },
      children: [new TextRun({
        font: FONT, size: 19, color: C.gold, bold: true, text: label,
      })],
    }),
    new Paragraph({
      spacing: { after: 60 },
      border: { bottom: { style: BorderStyle.SINGLE, color: C.navy, size: 6 } },
      children: [new TextRun({
        font: FONT, size: 34, color: C.navy, bold: true, text: title,
      })],
    }),
    spacer(140),
  ];
}

// ── Helper: H2 with teal ■ prefix ──────────────────────────────────
function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 360, after: 100 },
    children: [new TextRun({
      font: FONT, size: 26, color: C.teal, bold: true,
      text: `\u25A0 ${text}`,
    })],
  });
}

// ── Helper: H3 ─────────────────────────────────────────────────────
function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 260, after: 80 },
    children: [new TextRun({
      font: FONT, size: 23, color: C.navy, bold: true, text: text,
    })],
  });
}

// ── Helper: divider line ───────────────────────────────────────────
function divider() {
  return new Paragraph({
    spacing: { before: 80, after: 200 },
    border: { bottom: { style: BorderStyle.SINGLE, color: C.divider, size: 4 } },
    children: [new TextRun("")],
  });
}

// ── Helper: Bible verse highlight ──────────────────────────────────
function bibleBox(reference, verseText) {
  const border = { style: BorderStyle.SINGLE, color: C.teal, size: 4 };
  return new Table({
    width: { size: CONTENT_W, type: WidthType.DXA },
    columnWidths: [CONTENT_W],
    rows: [new TableRow({
      children: [new TableCell({
        width: { size: CONTENT_W, type: WidthType.DXA },
        borders: { top: border, bottom: border, left: border, right: border },
        shading: { fill: "F0F7FA", type: ShadingType.CLEAR },
        margins: { top: 100, left: 200, bottom: 100, right: 200 },
        children: [
          new Paragraph({
            spacing: { after: 40 },
            children: [new TextRun({
              font: FONT, size: 18, color: C.teal, bold: true, text: reference,
            })],
          }),
          new Paragraph({
            spacing: { after: 0 },
            children: [new TextRun({
              font: FONT, size: 20, color: C.body, italics: true, text: verseText,
            })],
          }),
        ],
      })],
    })],
  });
}

// ── Note box (일러두기) ────────────────────────────────────────────
function noteBox(title, text) {
  const border = { style: BorderStyle.THICK, color: C.goldDk, size: 6 };
  return new Table({
    width: { size: CONTENT_W, type: WidthType.DXA },
    columnWidths: [CONTENT_W],
    rows: [new TableRow({
      children: [new TableCell({
        width: { size: CONTENT_W, type: WidthType.DXA },
        borders: { top: border, bottom: border, left: border, right: border },
        shading: { fill: C.noteBg, type: ShadingType.CLEAR },
        margins: { top: 140, left: 220, bottom: 140, right: 220 },
        children: [
          new Paragraph({
            spacing: { after: 100 },
            children: [new TextRun({
              font: FONT, size: 22, color: C.goldDk, bold: true, text: title,
            })],
          }),
          new Paragraph({
            spacing: { after: 0, line: 380 },
            children: [new TextRun({
              font: FONT, size: 21, color: C.body, text,
            })],
          }),
        ],
      })],
    })],
  });
}

