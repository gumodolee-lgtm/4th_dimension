// ── Helpers ────────────────────────────────────────────────────────
function txt(text, opts = {}) {
  return new TextRun({ font: F, size: 21, color: C.body, ...opts, text });
}

function boldSpan(text) {
  return new TextRun({ font: F, size: 21, color: C.body, bold: true, text });
}

// Body paragraph supporting **bold** syntax
function p(text) {
  const runs = [];
  const parts = text.split(/(\*\*[^*]+\*\*)/g);
  for (const part of parts) {
    if (part.startsWith("**") && part.endsWith("**")) {
      runs.push(boldSpan(part.slice(2, -2)));
    } else {
      runs.push(txt(part));
    }
  }
  return new Paragraph({ spacing: { after: 140, line: 380 }, children: runs });
}

function sp(after = 80) {
  return new Paragraph({ spacing: { after }, children: [new TextRun("")] });
}

// Quote box (left gold border, cream bg)
function q(quote, src) {
  const nb = { style: BorderStyle.NONE, color: C.white, size: 0 };
  return new Table({
    width: { size: CW, type: WidthType.DXA }, columnWidths: [CW],
    rows: [new TableRow({ children: [new TableCell({
      width: { size: CW, type: WidthType.DXA },
      borders: { top: nb, bottom: nb, right: nb, left: { style: BorderStyle.THICK, color: C.gold, size: 22 } },
      shading: { fill: C.quoteBg, type: ShadingType.CLEAR },
      margins: { top: 140, left: 220, bottom: 140, right: 220 },
      children: [
        new Paragraph({ spacing: { after: 70, line: 380 }, children: [
          txt(quote, { size: 20, color: C.quoteText, italics: true }),
        ]}),
        new Paragraph({ spacing: { after: 0, line: 380 }, alignment: AlignmentType.RIGHT, children: [
          txt(`— ${src}`, { size: 18, color: C.gold, bold: true }),
        ]}),
      ],
    })]})],
  });
}

// Bible verse (same quote style but with reference as prefix)
function bv(ref, text) {
  const nb = { style: BorderStyle.NONE, color: C.white, size: 0 };
  return new Table({
    width: { size: CW, type: WidthType.DXA }, columnWidths: [CW],
    rows: [new TableRow({ children: [new TableCell({
      width: { size: CW, type: WidthType.DXA },
      borders: { top: nb, bottom: nb, right: nb, left: { style: BorderStyle.THICK, color: C.teal, size: 22 } },
      shading: { fill: "F0F7FA", type: ShadingType.CLEAR },
      margins: { top: 120, left: 220, bottom: 120, right: 220 },
      children: [
        new Paragraph({ spacing: { after: 40 }, children: [
          txt(ref, { size: 18, color: C.teal, bold: true }),
        ]}),
        new Paragraph({ spacing: { after: 0, line: 380 }, children: [
          txt(text, { size: 20, color: C.body, italics: true }),
        ]}),
      ],
    })]})],
  });
}

// Section header (e.g. "들어가며", "1부", "2부")
function sec(label, subtitle) {
  const children = [
    sp(300),
    new Paragraph({ spacing: { after: 40, line: 340 }, children: [
      txt(label, { size: 30, color: C.navy, bold: true }),
    ]}),
  ];
  if (subtitle) {
    children.push(new Paragraph({ spacing: { after: 120, line: 340 }, children: [
      txt(subtitle, { size: 24, color: C.body, bold: true }),
    ]}));
  }
  children.push(sp(60));
  return children;
}

// Method header (numbered method in 3부)
function method(num, title, subtitle) {
  return [
    sp(240),
    new Paragraph({ spacing: { after: 20, line: 340 }, children: [
      txt(String(num), { size: 32, color: C.navy }),
    ]}),
    new Paragraph({ spacing: { after: 20, line: 340 }, children: [
      txt(title, { size: 28, color: C.navy, bold: true }),
    ]}),
    new Paragraph({
      spacing: { after: 100, line: 340 },
      border: { bottom: { style: BorderStyle.SINGLE, color: C.navy, size: 4 } },
      children: [txt(subtitle, { size: 19, color: C.navy, bold: true })],
    }),
    sp(60),
  ];
}

// H2 for sub-sections in 1부/2부
function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 320, after: 100, line: 340 },
    children: [txt(text, { size: 24, color: C.navy, bold: true })],
  });
}

// Summary box
function summary(title, items) {
  const b = { style: BorderStyle.SINGLE, color: C.divider, size: 4 };
  const ch = [
    new Paragraph({ spacing: { after: 80 }, children: [
      txt(title, { size: 20, color: C.navy, bold: true }),
    ]}),
  ];
  for (const item of items) {
    ch.push(new Paragraph({ spacing: { after: 40, line: 360 }, children: [
      txt(`✓ ${item}`, { size: 19, color: C.body }),
    ]}));
  }
  return new Table({
    width: { size: CW, type: WidthType.DXA }, columnWidths: [CW],
    rows: [new TableRow({ children: [new TableCell({
      width: { size: CW, type: WidthType.DXA },
      borders: { top: b, bottom: b, left: b, right: b },
      shading: { fill: C.summaryBg, type: ShadingType.CLEAR },
      margins: { top: 120, left: 200, bottom: 120, right: 200 },
      children: ch,
    })]})],
  });
}

function divider() {
  return new Paragraph({
    spacing: { before: 80, after: 160 },
    border: { bottom: { style: BorderStyle.SINGLE, color: C.divider, size: 4 } },
    children: [new TextRun("")],
  });
}

